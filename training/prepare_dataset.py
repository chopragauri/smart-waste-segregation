"""
Step 2: Prepare YOLO-format dataset from downloaded images.

For each image:
  - Validates it can be opened
  - Resizes to 640x640
  - Creates YOLO-format annotation (auto-labels using center bounding box
    since search images typically have the object centered)
  - Splits into train/val (80/20)

Custom classes:
  0: tomato
  1: keys
  2: paper
"""

import os
import random
import shutil
from pathlib import Path
from PIL import Image

RAW_DIR = Path(__file__).parent / "raw_images"
DATASET_DIR = Path(__file__).parent / "dataset"

CLASSES = ["tomato", "keys", "paper"]
TRAIN_SPLIT = 0.8
IMG_SIZE = 640

# Auto-annotation: bounding box as fraction of image
# Search result images usually have the object taking up 50-80% of the frame
# We use a generous center box: x_center=0.5, y_center=0.5, w=0.7, h=0.7
AUTO_BBOX = (0.5, 0.5, 0.7, 0.7)  # YOLO format: x_center, y_center, width, height


def validate_and_resize(src_path, dst_path):
    """Validate image and resize to IMG_SIZE x IMG_SIZE."""
    try:
        img = Image.open(src_path).convert("RGB")
        img = img.resize((IMG_SIZE, IMG_SIZE), Image.LANCZOS)
        img.save(dst_path, "JPEG", quality=90)
        return True
    except Exception:
        return False


def create_yolo_label(class_id, label_path):
    """Create a YOLO format label file with auto bounding box."""
    x_c, y_c, w, h = AUTO_BBOX
    with open(label_path, "w") as f:
        f.write(f"{class_id} {x_c} {y_c} {w} {h}\n")


def prepare():
    print("=" * 60)
    print("Preparing YOLO dataset...")
    print("=" * 60)

    # Clean old dataset
    if DATASET_DIR.exists():
        shutil.rmtree(DATASET_DIR)

    # Create directory structure
    for split in ["train", "val"]:
        (DATASET_DIR / split / "images").mkdir(parents=True, exist_ok=True)
        (DATASET_DIR / split / "labels").mkdir(parents=True, exist_ok=True)

    total_train = 0
    total_val = 0

    for class_id, class_name in enumerate(CLASSES):
        raw_class_dir = RAW_DIR / class_name
        if not raw_class_dir.exists():
            print(f"  WARNING: No images found for '{class_name}' at {raw_class_dir}")
            continue

        # Collect all image files
        image_files = [
            f for f in raw_class_dir.iterdir()
            if f.suffix.lower() in ('.jpg', '.jpeg', '.png', '.bmp', '.webp')
        ]
        random.shuffle(image_files)

        split_idx = int(len(image_files) * TRAIN_SPLIT)
        train_files = image_files[:split_idx]
        val_files = image_files[split_idx:]

        for split_name, files in [("train", train_files), ("val", val_files)]:
            for i, src_file in enumerate(files):
                img_name = f"{class_name}_{i:04d}.jpg"
                lbl_name = f"{class_name}_{i:04d}.txt"

                img_dst = DATASET_DIR / split_name / "images" / img_name
                lbl_dst = DATASET_DIR / split_name / "labels" / lbl_name

                if validate_and_resize(src_file, img_dst):
                    create_yolo_label(class_id, lbl_dst)
                    if split_name == "train":
                        total_train += 1
                    else:
                        total_val += 1

        print(f"  {class_name}: {len(train_files)} train, {len(val_files)} val")

    # Create dataset.yaml
    yaml_content = f"""# Smart Waste Segregation - Custom Dataset
# Auto-generated by prepare_dataset.py

path: {DATASET_DIR.resolve()}
train: train/images
val: val/images

nc: {len(CLASSES)}
names: {CLASSES}
"""
    yaml_path = DATASET_DIR / "dataset.yaml"
    with open(yaml_path, "w") as f:
        f.write(yaml_content)

    print(f"\n  Total: {total_train} train images, {total_val} val images")
    print(f"  Dataset YAML: {yaml_path}")
    print("=" * 60)
    print("Dataset ready for training!")
    print("=" * 60)

    return str(yaml_path)


if __name__ == "__main__":
    prepare()
